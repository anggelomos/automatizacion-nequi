package co.com.automatizacion.nequi.data.dto;

public class DTONequiData {

    private String registerDate;
    private int disponible;
    private int bolsillos;
    private int colchon;
    private int metas;
    private int tarjeta;
    private int congelado;
    private int total;

    public String getRegisterDate() {
        return registerDate;
    }

    public void setRegisterDate(String registerDate) {
        this.registerDate = registerDate;
    }

    public int getDisponible() {
        return disponible;
    }

    public void setDisponible(int disponible) {
        this.disponible = disponible;
    }

    public int getBolsillos() {
        return bolsillos;
    }

    public void setBolsillos(int bolsillos) {
        this.bolsillos = bolsillos;
    }

    public int getColchon() {
        return colchon;
    }

    public void setColchon(int colchon) {
        this.colchon = colchon;
    }

    public int getMetas() {
        return metas;
    }

    public void setMetas(int metas) {
        this.metas = metas;
    }

    public int getTarjeta() {
        return tarjeta;
    }

    public void setTarjeta(int tarjeta) {
        this.tarjeta = tarjeta;
    }

    public int getCongelado() {
        return congelado;
    }

    public void setCongelado(int congelado) {
        this.congelado = congelado;
    }

    public int getTotal() {
        return total;
    }

    public void setTotal(int total) {
        this.total = total;
    }
}
